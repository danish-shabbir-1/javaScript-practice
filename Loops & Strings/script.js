// 1. For Loop

for(let i = 10; i <= 100; i = i+10) {
    console.log('10', 'x', i/5, '=', i);
}

// 2. While Loop

let i = 1

while (i <= 10) {
    console.log("Hello Beta =" + i);
    i++;
}

// 3. Do While Loop

let j = 20

do {
    console.log("Danish Here");
    j++
} while (j <= 10);










